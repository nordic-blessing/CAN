#include "stm32f4xx_it.h"
#include "systemInit.h"



float realtime=0;
float SpringAngelWant = 720.0f;
u8 angleCount = 0;

//5ms
void TIM8_TRG_COM_TIM14_IRQHandler(void)
{
	static u8 i=0;
	if(TIM_GetITStatus(TIM14, TIM_IT_Update)==SET)
	{
		TIM_ClearITPendingBit(TIM14, TIM_IT_Update);
		realtime++;		
		angleCount++;
		
		if(angleCount >= 2)
		{
			SetPoint(&S_Motor.AngleLoop,SpringAngelWant);
			S_Motor.SpeedLoop.Setpoint=PID_PosLocCalc(&S_Motor.AngleLoop,motoS_chassis.real_angle);
			angleCount = 0;
		}
		
		Speed_Control();
		
		
	}
}




